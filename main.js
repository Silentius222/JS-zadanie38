function funkcja(a, b){
    let dodawanie = a + b;
    if (dodawanie > 0) {
    console.log("Wynik dodawania wynosi: " + dodawanie)
  } else {
    console.log("Wynik jest nieprawidłowy")
  }

    let odejmowanie = a - b;
    if (odejmowanie > 0) {
    console.log("Wynik odejmowania wynosi: " + odejmowanie)
  } else {
    console.log("Wynik jest nieprawidłowy")
  }

    let mnozenie = a * b;
    if (mnozenie > 0) {
    console.log("Wynik mnozenia wynosi: " + mnozenie)
  } else {
    console.log("Wynik jest nieprawidłowy")
  }

}
